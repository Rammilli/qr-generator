/** Get a platform SVG as a data URI for use as a QR logo */
export function platformLogoDataUri(platform) {
  const colored = platform.svgIcon
    .replace(/currentColor/g, platform.color)
  const encoded = btoa(unescape(encodeURIComponent(colored)))
  return `data:image/svg+xml;base64,${encoded}`
}

export function getPlatform(id) {
  return PLATFORMS.find(p => p.id === id) || null
}
